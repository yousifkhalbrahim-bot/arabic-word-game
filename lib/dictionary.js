export function normalize(text) {
  return text
    .trim()
    .replace(/[\u064B-\u065F\u0670]/g, '')
    .replace(/[أإآ]/g, 'ا')
    .replace(/ى/g, 'ي');
}

export const NORMALIZED_DICT = new Set(ARABIC_DICTIONARY.map(normalize));
export function normalizeStrict(text) {
  return text
    .trim()
    .replace(/[ً-ٰٟ]/g, '')
    .replace(/[ىئ]/g, 'ي');
}

const _strictMap = new Map();
const _looseMap = new Map();
for (const w of ARABIC_DICTIONARY) {
  const sk = normalizeStrict(w);
  if (!_strictMap.has(sk)) _strictMap.set(sk, w);
  const lk = normalize(w);
  if (!_looseMap.has(lk)) _looseMap.set(lk, []);
  _looseMap.get(lk).push(w);
}

export function lookupWord(word) {
  if (_strictMap.size === 0) return { valid: true, canonical: word, ambiguous: false };
  const sk = normalizeStrict(word);
  if (_strictMap.has(sk)) return { valid: true, canonical: _strictMap.get(sk), ambiguous: false };
  const lk = normalize(word);
  const matches = _looseMap.get(lk) || [];
  if (matches.length === 0) return { valid: false, canonical: word, ambiguous: false };
  if (matches.length > 1) return { valid: false, canonical: word, ambiguous: true };
  return { valid: true, canonical: matches[0], ambiguous: false };
}
